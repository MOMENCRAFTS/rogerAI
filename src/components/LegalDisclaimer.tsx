function btnStyle(variant: 'ghost' | 'accept' | 'danger'): React.CSSProperties {
  const base: React.CSSProperties = {
    fontFamily: "'JetBrains Mono', monospace",
    fontSize: 10,
    letterSpacing: '0.2em',
    textTransform: 'uppercase',
    padding: '11px 20px',
    border: '1px solid',
    cursor: 'pointer',
    transition: 'all 0.25s ease',
    flex: variant === 'accept' ? 1 : 0,
    background: 'transparent',
  };
  if (variant === 'ghost') return { ...base, color: 'rgba(255,255,255,0.4)', borderColor: 'rgba(255,255,255,0.12)' };
  if (variant === 'danger') return { ...base, color: '#a84832', borderColor: 'rgba(168,72,50,0.4)', background: 'rgba(168,72,50,0.06)' };
  return { ...base, color: '#d4a044', borderColor: 'rgba(212,160,68,0.5)', background: 'rgba(212,160,68,0.08)' };
}

