interface Props {
  onAccept: () => void;
}

export default function LegalDisclaimer({ onAccept }: Props) {
  const scrollRef = useRef<HTMLDivElement>(null);
  const [canAccept, setCanAccept]     = useState(false);
  const [scrollPct, setScrollPct]     = useState(0);
  const [activeSection, setActiveSection] = useState<string | null>(null);
  const [showDeclineModal, setShowDeclineModal] = useState(false);
  const [declined, setDeclined]       = useState(false);
  const [acceptFlash, setAcceptFlash] = useState(false);
  const sectionRefs = useRef<Record<string, HTMLDivElement | null>>({});

  // ── Scroll tracking ──────────────────────────────────────────────────────
  function onScroll() {
    const el = scrollRef.current;
    if (!el) return;
    const pct = el.scrollTop / (el.scrollHeight - el.clientHeight);
    setScrollPct(pct);
    if (pct >= 0.97 && !canAccept) {
      setCanAccept(true);
      setAcceptFlash(true);
      setTimeout(() => setAcceptFlash(false), 1200);
    }
  }

  // ── Section highlighting via IntersectionObserver ────────────────────────
  useEffect(() => {
    const obs = new IntersectionObserver(
      entries => {
        entries.forEach(e => {
          if (e.isIntersecting) setActiveSection(e.target.id);
        });
      },
      { root: scrollRef.current, threshold: 0.4 }
    );
    Object.values(sectionRefs.current).forEach(el => el && obs.observe(el));
    return () => obs.disconnect();
  }, []);

  function handleAccept() {
    if (!canAccept) return;
    acceptLegal();
    onAccept();
  }

  // ── Tombstone screen ─────────────────────────────────────────────────────
  if (declined) {
    return (
      <div style={outerStyle}>
        <ScanlineOverlay />
        <AmbiGlow />
        <div style={{ position: 'relative', zIndex: 1, textAlign: 'center', padding: 32 }}>
          <div style={{ fontSize: 48, marginBottom: 24 }}>⛔</div>
          <p style={monoStyle(20, '#a84832', '0.28em')}>ACCESS DENIED</p>
          <p style={monoStyle(9, 'rgba(255,255,255,0.3)', '0.2em')}>
            You must accept the Terms &amp; Conditions to use ROGER·AI.<br />
            Close the application or restart to try again.
          </p>
        </div>
      </div>
    );
  }

  return (
    <div style={outerStyle}>
      <ScanlineOverlay />
      <AmbiGlow />

      {/* ── Decline confirmation modal ── */}
      {showDeclineModal && (
        <div style={modalOverlayStyle}>
          <div style={modalBoxStyle}>
            <BracketCorners />
            <p style={monoStyle(10, '#d4a044', '0.22em')}>⚠ CONFIRM DECLINE</p>
            <p style={{ ...bodyTextStyle, marginTop: 12, textAlign: 'center' }}>
              Declining will deny you access to ROGER·AI.<br />
              Are you sure you want to proceed?
            </p>
            <div style={{ display: 'flex', gap: 12, marginTop: 20 }}>
              <button style={btnStyle('ghost')} onClick={() => setShowDeclineModal(false)}>
                CANCEL
              </button>
              <button style={btnStyle('danger')} onClick={() => { setShowDeclineModal(false); setDeclined(true); }}>
                YES, DECLINE
              </button>
            </div>
          </div>
        </div>
      )}

      <div style={{ position: 'relative', zIndex: 1, display: 'flex', flexDirection: 'column', height: '100%', maxWidth: 680, margin: '0 auto', width: '100%' }}>

        {/* ── Header bar ── */}
        <div style={headerBarStyle}>
          <span style={monoStyle(8, 'rgba(212,160,68,0.45)', '0.3em')}>◈ CLASSIFIED</span>
          <span style={monoStyle(9, '#d4a044', '0.25em')}>LEGAL NOTICE · ROGER·AI</span>
          <span style={monoStyle(8, 'rgba(212,160,68,0.45)', '0.3em')}>CLASSIFIED ◈</span>
        </div>

        {/* ── Title block ── */}
        <div style={{ padding: '24px 24px 0', textAlign: 'center', flexShrink: 0 }}>
          <div style={{ fontSize: 32, marginBottom: 10 }}>⚖</div>
          <p style={monoStyle(22, 'rgba(255,255,255,0.92)', '0.28em')}>
            ROGER<span style={{ color: '#d4a044' }}>·</span>AI
          </p>
          <p style={monoStyle(9, '#d4a044', '0.2em')}>TERMS &amp; CONDITIONS OF USE</p>
          <p style={monoStyle(8, 'rgba(255,255,255,0.22)', '0.15em')}>
            VERSION 2.0 · EFFECTIVE 2026-04-25 · MOMENCRAFTS · RIYADH, KSA
          </p>
          <div style={dividerStyle} />
          <p style={{ ...bodyTextStyle, marginBottom: 0 }}>
            Please read these Terms &amp; Conditions carefully before using the Service.
            By tapping <strong>"I ACCEPT"</strong>, you agree to be legally bound by these terms.
          </p>
          <div style={dividerStyle} />
        </div>

        {/* ── Scrollable content ── */}
        <div
          ref={scrollRef}
          onScroll={onScroll}
          style={scrollZoneStyle}
        >
          {SECTIONS.map(sec => (
            <div
              key={sec.id}
              id={sec.id}
              ref={el => { sectionRefs.current[sec.id] = el; }}
              style={sectionStyle(activeSection === sec.id, !!sec.warn)}
            >
              <div style={{ display: 'flex', alignItems: 'baseline', gap: 10, marginBottom: 10 }}>
                <span style={monoStyle(8, activeSection === sec.id ? '#d4a044' : 'rgba(212,160,68,0.4)', '0.2em')}>
                  §{sec.num}
                </span>
                <span style={monoStyle(11, activeSection === sec.id ? '#d4a044' : 'rgba(255,255,255,0.6)', '0.18em')}>
                  {sec.title.toUpperCase()}
                </span>
                {sec.warn && (
                  <span style={{ marginLeft: 'auto', ...monoStyle(8, '#a84832', '0.12em') }}>
                    ⚠ CRITICAL
                  </span>
                )}
              </div>
              <p style={bodyTextStyle}>{sec.body}</p>
            </div>
          ))}

          {/* Bottom spacer */}
          <div style={{ height: 40 }} />
        </div>

        {/* ── Scroll progress bar ── */}
        <div style={progressTrackStyle}>
          <div style={{ ...progressFillStyle, width: `${Math.min(scrollPct * 100, 100).toFixed(1)}%` }} />
          {!canAccept && (
            <span style={{ ...monoStyle(7, 'rgba(212,160,68,0.5)', '0.18em'), position: 'absolute', right: 8, top: '50%', transform: 'translateY(-50%)' }}>
              ▼ SCROLL TO READ ALL
            </span>
          )}
        </div>

        {/* ── Footer buttons ── */}
        <div style={footerStyle}>
          <button
            id="btn-legal-decline"
            style={btnStyle('ghost')}
            onClick={() => setShowDeclineModal(true)}
          >
            DECLINE
          </button>
          <button
            id="btn-legal-accept"
            style={{
              ...btnStyle('accept'),
              opacity: canAccept ? 1 : 0.3,
              cursor: canAccept ? 'pointer' : 'not-allowed',
              boxShadow: acceptFlash ? '0 0 20px rgba(212,160,68,0.5), 0 0 40px rgba(212,160,68,0.25)' : (canAccept ? '0 0 10px rgba(212,160,68,0.2)' : 'none'),
              transition: 'all 0.4s ease',
            }}
            disabled={!canAccept}
            onClick={handleAccept}
          >
            ✓ I ACCEPT
          </button>
        </div>

        {/* ── Legal footer note ── */}
        <p style={{ ...monoStyle(7, 'rgba(255,255,255,0.12)', '0.12em'), textAlign: 'center', padding: '8px 24px 12px', flexShrink: 0 }}>
          © 2026 MOMENCRAFTS · RIYADH, SAUDI ARABIA · LEGAL@MOMENCRAFTS.COM
        </p>
      </div>

      <style>{`
        @keyframes legalGlow {
          0%, 100% { opacity: 0.6; }
          50% { opacity: 1; }
        }
        @keyframes scrollHint {
          0%, 100% { opacity: 0.5; transform: translateY(0); }
          50% { opacity: 1; transform: translateY(3px); }
        }
      `}</style>
    </div>
  );
}

// ─── Sub-components ───────────────────────────────────────────────────────────

function ScanlineOverlay() {
  return (
    <div style={{
      position: 'absolute', inset: 0, pointerEvents: 'none', zIndex: 0,
      backgroundImage: 'repeating-linear-gradient(0deg, transparent, transparent 2px, rgba(0,0,0,0.06) 2px, rgba(0,0,0,0.06) 4px)',
      opacity: 0.6,
    }} />
  );
}

function AmbiGlow() {
  return (
    <>
      <div style={{
        position: 'absolute', top: '10%', left: '50%',
        transform: 'translate(-50%,-50%)',
        width: 600, height: 600, borderRadius: '50%', pointerEvents: 'none',
        background: 'radial-gradient(circle, rgba(212,160,68,0.05) 0%, transparent 65%)',
        animation: 'legalGlow 6s ease-in-out infinite',
      }} />
      <div style={{
        position: 'absolute', bottom: '10%', right: '5%',
        width: 300, height: 300, borderRadius: '50%', pointerEvents: 'none',
        background: 'radial-gradient(circle, rgba(168,72,50,0.04) 0%, transparent 65%)',
      }} />
    </>
  );
}

function BracketCorners() {
  const corner = (style: React.CSSProperties) => (
    <div style={{ position: 'absolute', width: 12, height: 12, ...style }}>
      <div style={{ position: 'absolute', top: 0, left: 0, width: 8, height: 1, background: '#d4a044' }} />
      <div style={{ position: 'absolute', top: 0, left: 0, width: 1, height: 8, background: '#d4a044' }} />
    </div>
  );
  return (
    <>
      {corner({ top: 8, left: 8 })}
      {corner({ top: 8, right: 8, transform: 'scaleX(-1)' })}
      {corner({ bottom: 8, left: 8, transform: 'scaleY(-1)' })}
      {corner({ bottom: 8, right: 8, transform: 'scale(-1)' })}
    </>
  );
}

